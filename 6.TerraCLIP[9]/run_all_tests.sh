#!/bin/bash

# ==========================================
# 🔧 这里的参数由您根据每次的需求来修改
# ==========================================

# 1. 设置您当前要测试的 benchmark 名字
BENCHMARK="benchmark2"

# 2. 设置实验的数量 n (如果 n=4，就会按顺序跑 exp1, exp2, exp3, exp4)
MAX_EXP=6

# 3. 设置使用的 GPU 编号
GPU_ID="0"

# ==========================================

echo "🌟 开始批量测试 | 目标: ${BENCHMARK} | 实验总数: ${MAX_EXP}"
echo "----------------------------------------------------------"

# 循环从 1 到 n
for i in $(seq 1 $MAX_EXP)
do
    # 🚩 新增：跳过 exp7
    if [ ${i} -eq 7 ]; then
        echo ""
        echo "=========================================================="
        echo "🔶 跳过: ${BENCHMARK} -> exp7"
        echo "=========================================================="
        continue
    fi

    EXP_NAME="exp${i}"
    
    echo ""
    echo "=========================================================="
    echo "🚀 正在运行: ${BENCHMARK} -> ${EXP_NAME} (使用 GPU: ${GPU_ID})"
    echo "=========================================================="
    
    # 这里的 test_clip_final.py 请替换为您实际保存的 Python 脚本文件名
    python test_clip_final.py \
        --benchmark ${BENCHMARK} \
        --exp_name ${EXP_NAME} \
        --gpu ${GPU_ID}
        
    echo "✅ ${EXP_NAME} 运行完毕！测试结果已保存至对应的 checkpoint 文件夹。"
done

